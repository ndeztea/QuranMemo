<p><strong>Tajwid</strong>  menurut bahasa melakukan sesuatu dengan elok dan indah atau bagus dan membaguskan, tajwid berasal dari kata Jawwada (جوّد-يجوّد-تجويدا) dalam bahasa Arab. Dalam ilmu Qiraah, tajwid berarti mengeluarkan huruf dari tempatnya dengan memberikan sifat-sifat yang dimilikinya. Jadi ilmu tajwid adalah suatu ilmu yang mempelajari bagaimana cara membunyikan atau mengucapkan huruf-huruf yang terdapat dalam kitab suci al-Quran maupun bukan.
</p>

